package Modulo1_Fundamentos_Java.Aula14_SequenciasDeScape;

public class SequenciasScape {
    public static void main(String[] args) {
        System.out.println("Imprimindo string com somente uma palavra entre aspas.Usamos contra barras e chamamos de scape");
        System.out.println("Oi \"Maria\".");
        System.out.println("___________________________________________");
        System.out.println("Outro exemplo de sequência de scape");
        System.out.println("Seu nome: \nJoão (Contra barra n é usado para quebrar linha)");
        System.out.println("___________________________________________");
        System.out.println("Sempre que precisar de uma barra,coloca duas.Exemplo:");
        System.out.println("c:\\Windows");
        System.out.println("ESSAS SÃO AS PRINCIPAIS SEQUENCIAS DE SCAPE");

    }
}
